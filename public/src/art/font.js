export const FONT_CHARS = Object.keys(F);
const FRAME_PREFIX = 'g';

// Bake all glyphs (white) into one texture with named frames.
export function buildFont(scene) {
  if (scene.textures.exists('font')) return;
  const cols = 16;
  const rows = Math.ceil(FONT_CHARS.length / cols);
  const cw = GLYPH_W + 1, chh = GLYPH_H + 1;
  const g = scene.make.graphics({ x: 0, y: 0, add: false });
  FONT_CHARS.forEach((ch, i) => {
    const gx = (i % cols) * cw, gy = Math.floor(i / cols) * chh;
    const rowsData = F[ch];
    g.fillStyle(0xffffff, 1);
    for (let y = 0; y < GLYPH_H; y++) {
      const bits = rowsData[y];
      for (let x = 0; x < GLYPH_W; x++) {
        if (bits & (0x10 >> x)) g.fillRect(gx + x, gy + y, 1, 1);
      }
    }
  });
  g.generateTexture('font', cols * cw, rows * chh);
  g.destroy();
  const tex = scene.textures.get('font');
  FONT_CHARS.forEach((ch, i) => {
    const gx = (i % cols) * cw, gy = Math.floor(i / cols) * chh;
    tex.add(FRAME_PREFIX + ch.charCodeAt(0), 0, gx, gy, GLYPH_W, GLYPH_H);
  });
}

function frameFor(ch) {
  return F[ch] ? FRAME_PREFIX + ch.charCodeAt(0) : FRAME_PREFIX + 63; // '?' fallback
}

// Measure width of a single-line string at scale.
export function textWidth(str, scale = 1) {
  return str.length * ADV * scale;
}

// Word-wrap text to maxWidth px at scale. Returns array of lines.
// Honors explicit \n.
export function wrapText(str, maxWidth, scale = 1) {
  const out = [];
  for (const para of String(str).split('\n')) {
    const words = para.split(' ');
    let line = '';
    for (const w of words) {
      const test = line ? line + ' ' + w : w;
      if (textWidth(test, scale) > maxWidth && line) { out.push(line); line = w; }
      else line = test;
    }
    out.push(line);
  }
  return out;
}

// ── PixelText ──────────────────────────────────────────
// Container of glyph images. Crisp at integer scales.
export class PixelText extends Phaser.GameObjects.Container {
  constructor(scene, x, y, text, opts = {}) {
    super(scene, x, y);
    this.opts = {
      scale: opts.scale || 1,
      color: opts.color !== undefined ? opts.color : 0xe8e8f4,
      shadow: opts.shadow !== undefined ? opts.shadow : true,
      shadowColor: opts.shadowColor !== undefined ? opts.shadowColor : 0x141026,
      align: opts.align || 'left',       // left | center | right
      maxWidth: opts.maxWidth || 0,      // 0 = no wrap
      lineH: opts.lineH || LINE_H
    };
    scene.add.existing(this);
    this.setText(text);
  }

  setText(text) {
    this._text = String(text == null ? '' : text);
    const o = this.opts;
    const lines = o.maxWidth ? wrapText(this._text, o.maxWidth, o.scale) : this._text.split('\n');
    this._lines = lines;
    let widest = 0;
    lines.forEach(l => { widest = Math.max(widest, textWidth(l, o.scale)); });
    this.textW = widest;
    this.textH = lines.length * o.lineH * o.scale;
    let glyphIndex = 0;
    const placeGlyph = (frame, x, y, tint, scale) => {
      let image = this.list[glyphIndex];
      if (!image) {
        image = this.scene.add.image(x, y, 'font', frame).setOrigin(0, 0);
        this.add(image);
      }
      image.setTexture('font', frame).setPosition(x, y).setTint(tint).setScale(scale).setAlpha(1).setVisible(true);
      glyphIndex++;
    };
    lines.forEach((line, li) => {
      const lw = textWidth(line, o.scale);
      let ox = 0;
      if (o.align === 'center') ox = Math.round((widest - lw) / 2);
      if (o.align === 'right') ox = widest - lw;
      for (let i = 0; i < line.length; i++) {
        const ch = line[i];
        if (ch === ' ') continue;
        const fx = ox + i * ADV * o.scale;
        const fy = li * o.lineH * o.scale;
        if (o.shadow) {
          placeGlyph(frameFor(ch), fx + o.scale, fy + o.scale, o.shadowColor, o.scale);
        }
        placeGlyph(frameFor(ch), fx, fy, o.color, o.scale);
      }
    });
    for (let i = glyphIndex; i < this.list.length; i++) this.list[i].setVisible(false);
    return this;
  }

  setColor(color) {
    this.opts.color = color;
    return this.setText(this._text);
  }

  get text() { return this._text; }
}

// Convenience factory
export function ptext(scene, x, y, text, opts) {
  return new PixelText(scene, x, y, text, opts);
}
