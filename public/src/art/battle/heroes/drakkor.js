// 0xff4400, eyes 0xffcc33, outline 0x1a1a2a.
//
//   idle    — greataxe held upright at rest beside the body
//   step    — advance one body-length, axe still at rest
//   attack  — two-hand greataxe overhead chop, blade arcing down
//             toward the enemy line
//   cast    — Inferno Breath windup: chest seam and snout embers
//             brighten as he rears back to breathe fire
//   hit     — knocked back, draconic eyes shut
//   ko      — collapsed, greataxe dropped aside
//   victory — roars (jaw wide) + small flame puff above the snout
//
// Grid: 64 rows x 48 chars each, all rows normalized to 48 chars.
// ═══════════════════════════════════════════════════════════════

const W = 48, H = 64;
function norm(rows) {
  return rows.map(r => r.padEnd(W, '.').slice(0, W)).concat(
    Array(Math.max(0, H - rows.length)).fill('.'.repeat(W))
  ).slice(0, H);
}

